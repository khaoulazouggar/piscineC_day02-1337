void	ft_putchar(char c);

void	ft_putnbr(int nb)
{
	if (nb > 0 && nb != 0 && nb < 10)
		ft_putchar('0' + nb);
	else if (nb < 0)
	{
		ft_putchar('-');
		ft_putnbr(nb * (-1));
	}
	else
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
}
